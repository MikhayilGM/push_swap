#include "push_swap.h"

int	valid(char *str)
{
	if(*str != '-' && *str != '+' && !(*str >= '0' && *str <= '9'))
		return 0;
	++str;
	while(*str)
	{
		if(!(*str >= '0' && *str <= '9'))
			return 0;
		++str;
	}
	return 1;
}